package day13loops;

public class Loops01 {
    public static void main(String[] args) {
        System.out.println("Hi..");
        System.out.println("Hi..");
        System.out.println("Hi..");
        System.out.println("Hi..");
        System.out.println("Hi..");
        ///////for loop////////
        for(  int i = 1 ; i <6  ;  i++ ){
            System.out.println("Hi!");
        }

        ///Örnek 2: 11'den 14'e kadar tüm sayıları ekrana yazdıran kodu yazınız
        for(int i = 11; i <15 ; i++){
            System.out.println(i);
        }
        ///Örnek 3: 40'dan 23'e kadar tüm çift sayıları ekrana yazdıran kodu yazınız.
        for(int i = 40 ; i>22 ; i--){
            if(i%2==0){
                System.out.print(i+" ");
            }

        for (int a = 18; a < 56 ; a++){
            if (a%2==1) {
                System.out.print(a + " ");
            }
            }
        }
    }
}
